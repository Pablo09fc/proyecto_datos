    private void btn_ingresar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresar1ActionPerformed

        String nombre_usuario = txt_usuario.getText();
        String password = String.valueOf(txt_password.getPassword());
        
        if ((nombre_usuario.length() == 0) ||  (password.length()==0)){
            JOptionPane.showMessageDialog(null, "Se Debe Digitar un Usuario y Contraseña Válidos");
            return;
        }
        if(nombre_usuario.equals("Restaurante")&&(password.equals("1234"))){            
            Juego principal = new Juego();
            principal.setTitle("Formulario Principal del Sistema de hamburguesas");
            principal.setLocationRelativeTo(null);
            principal.setVisible(true);
            this.setVisible(false);
            principal.pack();  
        }else{
            JOptionPane.showMessageDialog(null, "Los Datos de Ingreso No Son Correctos"+
                "Por Favor Intente de Nuevo");
        }   
    }//GEN-LAST:event_btn_ingresar1ActionPerformed

